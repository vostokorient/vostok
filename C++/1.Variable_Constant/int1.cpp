#include <iostream>
using namespace std;

int main()

{
    int r;
    // Определение параметров круга по данному радиусу
    cout << "Введите радиус круга \n \n";
    cin >> r;
    cout << "Диаметр круга:       " << 2 * r << "\n";
    cout << "Площадь круга:       " << 3.14159 * (r * r) << "\n";
    cout << "Длина окружности:    " << 2 * 3.14159 * r << "\n";
    return 0;
}